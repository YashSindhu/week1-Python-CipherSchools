name = "YaSh SiNdHu"
# 1.) len() function:-
print(len(name)) # it gives --> 11

# 2.) lower() method:-(Makes every letter small)
print(name.lower()) # it gives --> yash sindhu

# 3.) upper() method:-(Makes every letter capital)
print(name.upper()) # it gives --> YASH SINDHU

# 4.) title() method:-(Makes first letter capital)
print(name.title()) # it gives --> Yash Sindhu

# 5.) count() method:-(Counts a specific letter)
print(name.count("h")) # it gives -->1